"""
    hasil; dari opersi komperasi
    selalu bertipe data boolean
    (true/flase)
"""

# >,<,>=,<=,==, !=, is, dan is not

a = 4
b = 2

# lebih dari >
print("===lebih dari (>)")
hasil = a > 3 # TRUE
print(a, ">", 3, "=", hasil)
hasil = b > 3 # FALSE 
print(b, ">", 3, "=", hasil)
hasil = b > 2
print(b, ">", 2, "=", hasil)

# kurang dari <
print("===lebih dari (<)")
hasil = a < 3 # TRUE
print(a, "<", 3, "=", hasil)
hasil = b < 3 # FALSE 
print(b, "<", 3, "=", hasil)
hasil = b > 2
print(b, "<", 2, "=", hasil)